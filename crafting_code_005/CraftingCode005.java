import java.util.Scanner;

public class CraftingCode005 {
    public static void main(String[] args) {
        System.out.println("INPUT");
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        System.out.println("OUTPUT");
        for(int i=1;i<=n;i++){
            System.out.print("* ");
        }
        System.out.println();
    }
}
